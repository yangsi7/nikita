#!/usr/bin/env node
/**
 * Reads every *.html under src/app/admin/systems/_art/ and emits a
 * TypeScript module at src/app/admin/systems/_art-generated.ts that
 * inlines the HTML as a string-keyed record.
 *
 * This lets the [slug] route handler serve art via a pure JS import
 * instead of fs.readFile at request time — the bundler always includes
 * the content, so there's no deployment-tracing concern on Vercel.
 *
 * Run: `npm run generate-art` (fires automatically via predev/prebuild).
 */

import { readFileSync, writeFileSync, readdirSync } from "node:fs"
import { join, dirname } from "node:path"
import { fileURLToPath } from "node:url"

const __dirname = dirname(fileURLToPath(import.meta.url))
const ROOT = join(__dirname, "..")
const SRC = join(ROOT, "src/app/admin/systems/_art")
const DST = join(ROOT, "src/app/admin/systems/_art-generated.ts")

const files = readdirSync(SRC)
  .filter((f) => f.endsWith(".html"))
  .sort()

const entries = files
  .map((f) => {
    const slug = f.replace(/\.html$/, "")
    const html = readFileSync(join(SRC, f), "utf-8")
    return `  ${JSON.stringify(slug)}: ${JSON.stringify(html)},`
  })
  .join("\n")

const out = `// AUTO-GENERATED by portal/scripts/generate-art-module.mjs — DO NOT EDIT.
// Run \`npm run generate-art\` (or the predev/prebuild hook) to regenerate.
// Sourced from src/app/admin/systems/_art/*.html.

export const ART_HTML: Readonly<Record<string, string>> = {
${entries}
} as const
`

writeFileSync(DST, out, "utf-8")
console.log(`[generate-art] wrote ${DST} (${files.length} files)`)
